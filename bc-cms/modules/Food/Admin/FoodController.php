namespace Modules\Food\Admin;

use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use Modules\AdminController;
use Modules\Core\Events\CreatedServicesEvent;
use Modules\Core\Events\UpdatedServiceEvent;
use Modules\Food\Models\Food;
use Modules\Food\Models\FoodTerm;
use Modules\Food\Models\FoodTranslation;
use Modules\Core\Models\Attributes;
use Modules\Location\Models\Location;
use Modules\Location\Models\LocationCategory;

class FoodController extends AdminController
{
    protected $food;
    protected $food_translation;
    protected $food_term;
    protected $attributes;
    protected $location;
    private $locationCategoryClass;

    public function __construct(Food $food, FoodTranslation $food_translation, FoodTerm $food_term, Attributes $attributes, Location $location,LocationCategory $locationCategoryClass)
    {
        $this->setActiveMenu(route('food.admin.index'));
        $this->food = $food;
        $this->food_translation = $food_translation;
        $this->food_term = $food_term;
        $this->attributes = $attributes;
        $this->location = $location;
        $this->locationCategoryClass = $locationCategoryClass;
    }

    public function callAction($method, $parameters)
    {
        if (!Food::isEnable()) {
            return redirect('/');
        }
        return parent::callAction($method, $parameters); // TODO: Change the autogenerated stub
    }

    public function index(Request $request)
    {
        $this->checkPermission('food_view');
        $query = $this->food::query();
        $query->orderBy('id', 'desc');
        if (!empty($s = $request->input('s'))) {
            $query->where('title', 'LIKE', '%' . $s . '%');
            $query->orderBy('title', 'asc');
        }
        if (!empty($is_featured = $request->input('is_featured'))) {
            $query->where('is_featured', 1);
        }
        if (!empty($location_id = $request->query('location_id'))) {
            $query->where('location_id', $location_id);
        }
        if ($this->hasPermission('food_manage_others')) {
            if (!empty($author = $request->input('vendor_id'))) {
                $query->where('author_id', $author);
            }
        } else {
            $query->where('author_id', Auth::id());
        }
        $data = [
            'rows'                => $query->with(['author'])->paginate(20),
            'food_manage_others' => $this->hasPermission('food_manage_others'),
            'breadcrumbs'         => [
                [
                    'name' => __('Foods'),
                    'url'  => route('food.admin.index')
                ],
                [
                    'name'  => __('All'),
                    'class' => 'active'
                ],
            ],
            'page_title'          => __("Food Management")
        ];
        return view('Food::admin.index', $data);
    }

    public function recovery(Request $request)
